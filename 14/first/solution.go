func main() {

	startTime := time.Now()

	var rows = strings.Split(inputs, "\n")

	// MOVE ROCKS UP
	var mapp [][]rune

	// CREATE MAP
	for _, row := range rows {
		mapp = append(mapp, []rune(row))
	}

	// MOVE ROCKS UP
	moveRocksUp(mapp)

	res := countRes(mapp)

	elapsedTime := time.Since(startTime)
	fmt.Printf("Your function took %s\n", elapsedTime)

	fmt.Println(res)
}

func countRes(mapp [][]rune) int {
	nRows := len(mapp[0])
	res := 0
	for row := range mapp {
		for col := range mapp[row] {
			if mapp[row][col] == 'O' {
				res += nRows - row
			}
		}
	}
	return res
}

func moveRocksUp(mapp [][]rune) {
	for row := range mapp {
		for col := range mapp[row] {
			if mapp[row][col] == 'O' {
				moveRockUp(mapp, row, col)
			}
		}
	}
}

func moveRockUp(mapp [][]rune, row int, col int) {
	for row > 0 {
		if mapp[row-1][col] == '.' {
			mapp[row-1][col] = 'O'
			mapp[row][col] = '.'
		} else {
			break
		}
		row--
	}
}

