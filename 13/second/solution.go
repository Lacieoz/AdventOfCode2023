func main() {

	startTime := time.Now()

	var rows = strings.Split(inputs, "\n")

	res := 0
	ind := 0

	for ind < len(rows) {
		var patternTable []string

		// define pattern table
		for ind < len(rows) {
			row := strings.Replace(rows[ind], " ", "", -1)
			ind++
			if len(row) == 0 {
				break
			}

			patternTable = append(patternTable, row)
		}

		res += 100 * findUpRows(patternTable)
		patternTable = transpose(patternTable)
		res += findUpRows(patternTable)
	}
	elapsedTime := time.Since(startTime)
	fmt.Printf("Your function took %s\n", elapsedTime)

	fmt.Println(res)
}

func transpose(table []string) []string {
	var table2 []string

	for i := 0; i < len(table[0]); i++ {
		var newRow string = ""
		for k := 0; k < len(table); k++ {
			newRow = newRow + string(table[k][i])
		}
		table2 = append(table2, newRow)
	}
	return table2
}

func findUpRows(table []string) int {
	res := 0

	startRow := table[0]

	for i := 1; i < len(table); i++ {
		if stringDistance(startRow, table[i]) <= 1 && i%2 != 0 {
			var patternLine float32 = float32(i) / 2
			if checkPatternLine(0, patternLine, table) {
				res += int(patternLine + 0.5)
				break
			}
		}
	}

	endRow := table[len(table)-1]
	for i := len(table) - 2; i > -1; i-- {
		if stringDistance(endRow, table[i]) <= 1 && i%2 != 0 {
			var patternLine float32 = (float32(i) + float32(len(table)-1)) / 2
			if checkPatternLine(len(table)-1, patternLine, table) {
				res += int(patternLine + 0.5)
				break
			}
		}
	}

	return res
}

func checkPatternLine(i int, line float32, table []string) bool {
	dist := 0
	if float32(i) < line {
		for float32(i) < line {
			var opposite = int((line-float32(i))*2) + i
			dist += stringDistance(table[opposite], table[i])
			if dist > 1 {
				return false
			}
			i++
		}
	} else {
		for float32(i) > line {
			var opposite = int(line - (float32(i) - line))
			dist += stringDistance(table[opposite], table[i])
			if dist > 1 {
				return false
			}
			i--
		}
	}
	if dist == 1 {
		return true
	}
	return false

}

func stringDistance(a string, b string) int {
	res := 0

	for i := 0; i < len(a); i++ {
		if a[i] != b[i] {
			res++
		}
	}

	return res
}

